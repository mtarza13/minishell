#include "../../include/minishell.h"

int	ft_isspace(int c)
{
	return (c == ' ' || c == '\t' || c == '\n');
}

int	is_operater_char(char in)
{
	if (in == '|' || in == '>' || in == '<')
		return (1);
	return (0);
}
